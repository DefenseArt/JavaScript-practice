//산술 연산자
let a = 1 + 2;
console.log(a);

let b = 1 + 2 - (3 * 4) / 4;
console.log(b);

let c = 1;
c++;
++c;
console.log(c);

//대입 연산자
let d = 1;
d += 3; // d = d + 3

const e = !false;
console.log(e); //e 값은 true

const f = true && true;
console.log(f); //f값은 true;

const g = true || false;
console.log(g); //g값은 true
